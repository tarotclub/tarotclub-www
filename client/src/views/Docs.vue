            <p>Sous Windows, il s'agit de:</p>

            <pre>
            C:\Users\votre compte\.tarotclub

            Exemple: C:\Users\robert\.tarotclub
            </pre>

            <p>Sous Linux, il s'agit de:</p>

            <pre>
            /home/votre compte/.tarotclub

            Exemple: /home/robert/.tarotclub
            </pre>

            <p>Ce répertoire contient trois sous répertoires:</p>

            <ul>
                <li><b>avatars</b> : contient le cache des avatars des adversaires, utilisé pour améliorer la rapidité du jeu lors des parties en ligne</li>
                <li><b>games</b> : contient toutes vos parties jouées localement</li>
                <li><b>logs</b> : contient les journaux de déboggage du jeu. Un fichier par jour est généré, au format CSV</li>
            </ul>

